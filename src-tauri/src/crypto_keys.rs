/// XOR 混淆密钥
const XOR_KEY: u8 = 0x5A;

/// 解混淆并获取 RSA 公钥
///
/// # Returns
///
/// 返回 PEM 格式的 RSA 公钥字符串
pub fn get_rsa_public_key() -> String {
    // 解混淆
    let deobfuscated: Vec<u8> = OBFUSCATED_PUBLIC_KEY
        .iter()
        .map(|&b| b ^ XOR_KEY)
        .collect();

    // 转换为字符串
    let base64_key = String::from_utf8(deobfuscated)
        .expect("Failed to decode obfuscated key");

    // 按照 PEM 标准格式化（每行 64 字符）
    let mut formatted_lines = Vec::new();
    let chars: Vec<char> = base64_key.chars().collect();

    for chunk in chars.chunks(64) {
        formatted_lines.push(chunk.iter().collect::<String>());
    }

    let formatted_key = formatted_lines.join("\n");

    // 构建 PEM 格式
    format!(
        "-----BEGIN PUBLIC KEY-----\n{}\n-----END PUBLIC KEY-----",
        formatted_key
    )
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_deobfuscate_key() {
        let key = get_rsa_public_key();
        println!("解混淆后的公钥:\n{}", key);
        assert!(key.starts_with("-----BEGIN PUBLIC KEY-----"));
        assert!(key.ends_with("-----END PUBLIC KEY-----"));

        // 移除所有空白字符后检查
        let key_no_whitespace: String = key.chars().filter(|c| !c.is_whitespace()).collect();
        println!("无空白字符的公钥: {}", key_no_whitespace);

        // 检查关键部分
        assert!(key_no_whitespace.contains("-----BEGINPUBLICKEY-----"));
        assert!(key_no_whitespace.contains("-----ENDPUBLICKEY-----"));
        assert!(key_no_whitespace.contains("MIIBIjANBgkqhkiG9w0BAQE"));
        assert!(key_no_whitespace.contains("2GcewHVbvBaYS6krVsQQG"));
    }
}
